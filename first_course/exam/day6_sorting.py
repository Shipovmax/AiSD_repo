"""
День 6: Эффективные сортировки и Предиктивный поиск.
Фокус: Сравнение квадратичных и логарифмических алгоритмов, случайный опорный элемент,
интерполяционный поиск для равномерно распределенных данных.
"""

import random
from typing import List, Optional

# -----------------------------------------------------------------------------
# Задача 1: Сортировка Шейкера (Shaker Sort / Bidirectional Bubble Sort)
# Это улучшенная версия пузырьковой сортировки, которая гоняет элементы в обе стороны.
# -----------------------------------------------------------------------------

def shaker_sort(arr: List[int]) -> List[int]:
    """
    Реализация сортировки Шейкера.

    Логика работы:
    1. Проходим по массиву слева направо, вытесняя самый большой элемент в конец.
    2. Затем проходим справа налево, вытесняя самый маленький элемент в начало.
    3. Повторяем процесс, сужая границы (left, right), пока не перестанут происходить обмены.
    4. Это эффективнее обычного Bubble Sort, так как "тянет" маленькие элементы в начало быстрее.

    Сложность:
    - Временная: O(N^2) в худшем, O(N) если массив уже отсортирован.
    - Пространственная: O(1) — сортировка происходит in-place.
    """
    left = 0
    right = len(arr) - 1
    swapped = True

    while swapped:
        swapped = False
        # Проход вперед (как в пузырьке)
        for i in range(left, right):
            if arr[i] > arr[i + 1]:
                arr[i], arr[i + 1] = arr[i + 1], arr[i]
                swapped = True
        if not swapped:
            break
        right -= 1

        swapped = False
        # Проход назад
        for i in range(right, left, -1):
            if arr[i - 1] > arr[i]:
                arr[i - 1], arr[i] = arr[i], arr[i - 1]
                swapped = True
        left += 1
    return arr

# -----------------------------------------------------------------------------
# Задача 2: Сортировка Расческойкой (Comb Sort)
# Улучшение пузырьковой сортировки, которая использует "разрыв" (gap) между сравниваемыми элементами.
# -----------------------------------------------------------------------------

def comb_sort(arr: List[int]) -> List[int]:
    """
    Реализация сортировки расческойкой.

    Логика работы:
    1. Начинаем с большого разрыва (gap = len(arr)).
    2. На каждом шаге уменьшаем gap в 1.3 раза.
    3. Сравниваем и меняем элементы, которые находятся на расстоянии gap друг от друга.
    4. Когда gap становится равным 1, алгоритм фактически превращается в Bubble Sort, но к этому моменту
       массив уже почти отсортирован ("расчесан"), что ускоряет процесс.

    Сложность:
    - Временная: O(N log N) в среднем, O(N^2) в худшем.
    - Пространственная: O(1).
    """
    gap = len(arr)
    shrink = 1.3
    sorted_flag = False

    while not sorted_flag:
        gap = int(gap / shrink)
        if gap <= 1:
            gap = 1
            sorted_flag = True # Переходим в режим финальной дошлифовки (Bubble Sort)

        for i in range(0, len(arr) - gap):
            if arr[i] > arr[i + gap]:
                arr[i], arr[i + gap] = arr[i + gap], arr[i]
                sorted_flag = False
    return arr

# -----------------------------------------------------------------------------
# Задача 3: Быстрая сортировка (QuickSort) по Хоару с рандомизацией
# Стандарт индустрии. Главная проблема QuickSort — деградация до O(N^2) на отсортированных данных.
# Решение: использовать случайный опорный элемент (random pivot).
# -----------------------------------------------------------------------------

def quick_sort(arr: List[int]) -> List[int]:
    """
    Реализация QuickSort с использованием схемы разделения Хоара и рандомным пивотом.

    Логика работы:
    1. Выбираем случайный элемент как опорный (pivot).
    2. Разделяем массив на две части: элементы меньше pivot слева, больше — справа.
    3. Рекурсивно повторяем процесс для каждой из частей.
    4. Схема Хоара использует два указателя, которые движутся навстречу друг другу,
       что делает алгоритм более эффективным по количеству обменов.

    Сложность:
    - Временная: O(N log N) в среднем, O(N^2) в худшем. Рандомизация сводит вероятность худшего случая к минимуму.
    - Пространственная: O(log N) за счет стека рекурсии.
    """
    def _quick_sort(items: List[int], low: int, high: int):
        if low < high:
            p = _partition(items, low, high)
            _quick_sort(items, low, p)
            _quick_sort(items, p + 1, high)

    def _partition(items: List[int], low: int, high: int) -> int:
        # Рандомный пивот для защиты от O(N^2) на отсортированных данных
        pivot_idx = random.randint(low, high)
        pivot = items[pivot_idx]

        i = low - 1
        j = high + 1
        while True:
            i += 1
            while items[i] < pivot:
                i += 1
            j -= 1
            while items[j] > pivot:
                j -= 1
            if i >= j:
                return j
            items[i], items[j] = items[j], items[i]

    res = list(arr)
    _quick_sort(res, 0, len(res) - 1)
    return res

# -----------------------------------------------------------------------------
# Задача 4: Сортировка Слиянием (Merge Sort)
# Стабильная сортировка с гарантированным временем O(N log N).
# -----------------------------------------------------------------------------

def merge_sort(arr: List[int]) -> List[int]:
    """
    Реализация Merge Sort (Разделяй и властвуй).

    Логика работы:
    1. Если в массиве 1 элемент — он уже отсортирован.
    2. Делим массив пополам.
    3. Рекурсивно сортируем каждую половину.
    4. Сливаем (Merge) две отсортированные половины в один итоговый массив.

    Сложность:
    - Временная: O(N log N) всегда (гарантированная стабильность).
    - Пространственная: O(N) — требуется дополнительная память для временных списков при слиянии.
    """
    if len(arr) <= 1:
        return arr

    mid = len(arr) // 2
    left = merge_sort(arr[:mid])
    right = merge_sort(arr[mid:])

    return _merge(left, right)

def _merge(left: List[int], right: List[int]) -> List[int]:
    """Вспомогательная функция слияния двух отсортированных списков."""
    result = []
    i = j = 0
    while i < len(left) and j < len(right):
        if left[i] < right[j]:
            result.append(left[i])
            i += 1
        else:
            result.append(right[j])
            j += 1
    result.extend(left[i:])
    result.extend(right[j:])
    return result

# -----------------------------------------------------------------------------
# Задача 5: Интерполяционный поиск
# Продвинутая альтернатива бинарному поиску для равномерно распределенных данных.
# -----------------------------------------------------------------------------

def interpolation_search(arr: List[int], target: int) -> Optional[int]:
    """
    Реализация интерполяционного поиска.

    Логика работы:
    1. В отличие от бинарного поиска, который всегда берет середину, интерполяционный
       поиск "угадывает" позицию элемента, основываясь на его значении.
    2. Используется формула: pos = low + [ (target - arr[low]) * (high - low) / (arr[high] - arr[low]) ]
    3. Это похоже на то, как мы ищем слово в словаре: если слово на букву "Я", мы открываем конец книги, а не середину.

    Сложность:
    - Временная: O(log(log N)) в среднем (очень быстро), O(N) в худшем случае.
    - Пространственная: O(1).
    """
    low = 0
    high = len(arr) - 1

    while low <= high and arr[low] <= target <= arr[high]:
        if low == high:
            if arr[low] == target: return low
            return None

        # Формула интерполяции: вычисляем наиболее вероятную позицию элемента
        pos = low + int(((float(high - low) / (arr[high] - arr[low])) * (target - arr[low])))

        if arr[pos] == target:
            return pos
        if arr[pos] < target:
            low = pos + 1
        else:
            high = pos - 1
    return None

