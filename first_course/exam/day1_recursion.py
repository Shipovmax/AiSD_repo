from typing import Tuple, Final, Dict, List

# -----------------------------------------------------------------------------
# Задача 1: Метод Ньютона для вычисления квадратного корня
# Реализация вавилонского метода (метод Ньютона-Рафсона) через рекурсию.
# Цель: Найти sqrt(n) с заданной точностью (epsilon).
# -----------------------------------------------------------------------------

def recursive_sqrt_newton(n: float, guess: float = 1.0, epsilon: float = 1e-7) -> float:
    """
    Вычисляет квадратный корень числа, используя рекурсивный метод Ньютона.

    Логика работы:
    1. Мы берем начальное приближение (guess).
    2. Проверяем, достаточно ли близко квадрат нашего приближения к числу n.
    3. Если разница больше epsilon, вычисляем новое приближение по формуле:
       next_guess = (guess + n / guess) / 2
    4. Рекурсивно вызываем функцию с новым приближением до тех пор, пока не достигнем точности.

    Сложность:
    - Временная сложность: O(log(log(1/epsilon))) — метод обладает квадратичной сходимостью,
      что значит, что количество верных знаков после запятой удваивается на каждой итерации.
    - Пространственная сложность: O(log(log(1/epsilon))) — глубина рекурсивного стека
      соответствует количеству итераций.
    """
    # Базовый случай: если разница между guess^2 и n меньше точности, мы нашли корень
    if abs(guess * guess - n) < epsilon:
        return guess

    # Рекурсивный шаг: вычисление следующего приближения по формуле Ньютона
    next_guess = (guess + n / guess) / 2
    return recursive_sqrt_newton(n, next_guess, epsilon)

# -----------------------------------------------------------------------------
# Задача 2: Рекурсивная работа с цифрами числа (БЕЗ приведения к строке)
# Вычисляет сумму цифр положительного целого числа, используя операторы % и //.
# Это классическая задача на экзамене, чтобы проверить, не "чит" ли студент через str().
# -----------------------------------------------------------------------------

def sum_of_digits(n: int) -> int:
    """
    Вычисляет сумму цифр неотрицательного целого числа.

    Логика работы:
    1. Базовый случай: если число меньше 10, оно само является своей суммой цифр.
    2. Рекурсивный шаг:
       - n % 10 извлекает последнюю цифру числа.
       - n // 10 отбрасывает последнюю цифру.
       - Суммируем текущую цифру с результатом рекурсивного вызова для остальной части числа.

    Сложность:
    - Временная сложность: O(log10(n)) — количество вызовов равно количеству цифр в числе.
    - Пространственная сложность: O(log10(n)) — глубина стека равна количеству цифр.
    """
    # Базовый случай
    if n < 10:
        return n

    # Рекурсия: последняя цифра + сумма цифр остатка числа
    return (n % 10) + sum_of_digits(n // 10)

# -----------------------------------------------------------------------------
# Задача 3: Продвинутая последовательность Фибоначчи с мемоизацией
# Обычная рекурсия для Фибоначчи имеет экспоненциальную сложность O(2^n).
# Чтобы сделать её промышленной, используем словарь для кэширования результатов.
# -----------------------------------------------------------------------------

def fibonacci_memo(n: int, memo: Dict[int, int] = None) -> int:
    """
    Вычисляет n-ое число Фибоначчи с использованием рекурсии и мемоизации.

    Логика работы:
    1. Создаем словарь `memo` (если он не передан), где ключом будет номер числа, а значением — само число.
    2. Перед вычислением проверяем: если число уже есть в `memo`, возвращаем его мгновенно.
    3. Если числа нет, вычисляем его как сумму двух предыдущих (рекурсивно) и сохраняем в `memo`.

    Сложность:
    - Временная сложность: O(n) — каждое число от 0 до n вычисляется ровно один раз.
    - Пространственная сложность: O(n) — затраты на хранение словаря и стек рекурсии.
    """
    # Инициализация кэша при первом вызове
    if memo is None:
        memo = {0: 0, 1: 1}

    # Проверка кэша (мемоизация)
    if n in memo:
        return memo[n]

    # Рекурсивное вычисление и запись в кэш
    memo[n] = fibonacci_memo(n - 1, memo) + fibonacci_memo(n - 2, memo)
    return memo[n]

# -----------------------------------------------------------------------------
# Задача 4: Рекурсивное разделение (QuickSelect)
# Поиск k-го наименьшего элемента в неотсортированном списке.
# Это вариация QuickSort, но вместо сортировки всего массива мы идем только в одну ветку.
# -----------------------------------------------------------------------------

def quick_select(arr: List[int], k: int) -> int:
    """
    Находит k-ый наименьший элемент в списке (k начинается с 0).

    Логика работы:
    1. Выбираем опорный элемент (pivot).
    2. Разделяем массив на три части:
       - left: элементы меньше pivot.
       - mid: элементы равные pivot.
       - right: элементы больше pivot.
    3. Сравниваем k с размерами этих частей:
       - Если k < len(left), значит искомый элемент в левой части $\to$ рекурсия в left.
       - Если k < len(left) + len(mid), значит искомый элемент и есть pivot $\to$ возвращаем pivot.
       - Иначе искомый элемент в правой части $\to$ рекурсия в right с корректировкой индекса k.

    Сложность:
    - Временная сложность: O(n) в среднем, O(n^2) в худшем случае (если pivot всегда крайний).
    - Пространственная сложность: O(n) из-за создания новых списков при разделении.
    """
    # Базовый случай: в массиве остался один элемент
    if len(arr) == 1:
        return arr[0]

    # Выбор опорного элемента (берем средний по индексу)
    pivot = arr[len(arr) // 2]

    # Разделение массива (Partitioning)
    left = [x for x in arr if x < pivot]
    mid = [x for x in arr if x == pivot]
    right = [x for x in arr if x > pivot]

    # Рекурсивный спуск в нужную часть
    if k < len(left):
        return quick_select(left, k)
    elif k < len(left) + len(mid):
        return pivot # Элемент найден
    else:
        # Пересчитываем k, вычитая количество элементов в left и mid
        return quick_select(right, k - len(left) - len(mid))

