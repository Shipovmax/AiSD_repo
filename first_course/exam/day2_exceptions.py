"""
День 2: Исключения и Контрактное программирование.
Фокус: Создание иерархий исключений, проверка инвариантов через assert,
создание декораторов для валидации входных данных.
"""

from typing import Type, Optional, Callable, Any
from functools import wraps

# -----------------------------------------------------------------------------
# 1. Иерархия исключений
# В промышленном коде и в академических задачах важно не использовать общий Exception.
# Создается базовый класс для всех ошибок модуля и специфичные подклассы.
# -----------------------------------------------------------------------------

class AiSDError(Exception):
    """Базовый класс для всех исключений в модуле подготовки к АиСД."""
    pass

class ValidationError(AiSDError):
    """Вызывается, когда входные данные не соответствуют заданным ограничениям."""
    pass

class AlgorithmConvergenceError(AiSDError):
    """Вызывается, когда итерационный алгоритм не сошелся за заданное количество шагов."""
    pass

class OutOfBoundsError(AiSDError):
    """Вызывается при попытке доступа к элементу за пределами границ структуры данных."""
    pass

# -----------------------------------------------------------------------------
# 2. Контрактное программирование: Декоратор валидации
# Контракт — это соглашение: "Если ты дашь мне правильные данные, я гарантирую результат".
# Декоратор позволяет вынести проверку условий (предусловий) из основного тела функции.
# -----------------------------------------------------------------------------

def validate_preconditions(validator: Callable[[Any], bool], error_msg: str):
    """
    Декоратор, который проверяет предусловие перед выполнением функции.

    Логика работы:
    1. Принимает функцию-валидатор, которая должна вернуть True или False.
    2. Перехватывает вызов функции, проверяет первый аргумент с помощью валидатора.
    3. Если валидатор вернул False, выбрасывает ValidationError.
    """
    def decorator(func: Callable):
        @wraps(func)
        def wrapper(*args, **kwargs):
            # Проверяем первый аргумент (обычно это основной входной параметр)
            if not validator(args[0] if args else None):
                raise ValidationError(f"Нарушен контракт: {error_msg}")
            return func(*args, **kwargs)
        return wrapper
    return decorator

# -----------------------------------------------------------------------------
# Пример: Безопасное вычисление обратного числа с контрактом
# -----------------------------------------------------------------------------

def is_positive(n: float) -> bool:
    """Простая функция-валидатор: число должно быть строго больше нуля."""
    return n > 0

@validate_preconditions(is_positive, "Входное число должно быть строго положительным")
def safe_reciprocal(n: float) -> float:
    """
    Вычисляет 1/n.

    Логика:
    - Благодаря декоратору мы уверены, что n > 0.
    - Внутри мы используем assert для проверки внутренних инвариантов (которые никогда не должны быть нарушены).

    Сложность:
    - Временная: O(1)
    - Пространственная: O(1)
    """
    # Assert используется для отладки. Если n == 0, значит ошибка в самом декораторе или логике программы.
    assert n != 0, "Внутренняя ошибка: обнаружено деление на ноль"
    return 1.0 / n

# -----------------------------------------------------------------------------
# Пример: Кастомный валидатор для доступа к массиву
# Реализация проверки границ вручную, чтобы заменить стандартный IndexError на наш OutOfBoundsError.
# -----------------------------------------------------------------------------

class SafeArray:
    """Класс-обертка для списка, обеспечивающий строгое управление границами."""
    def __init__(self, data: list[int]):
        self._data = data

    def get_element(self, index: int) -> int:
        """
        Извлекает элемент по индексу с проверкой границ.

        Логика:
        1. Проверяем, входит ли индекс в диапазон [0, len(data)-1].
        2. Если нет — выбрасываем специализированное исключение OutOfBoundsError.

        Сложность:
        - Временная: O(1)
        - Пространственная: O(1)
        """
        if index < 0 or index >= len(self._data):
            raise OutOfBoundsError(f"Индекс {index} выходит за границы массива размера {len(self._data)}")
        return self._data[index]

if __name__ == "__main__":
    print("--- День 2: Тесты Исключений и Контрактов ---")

    # Тест 1: Успешный вызов функции с контрактом
    try:
        val = safe_reciprocal(10.0)
        print(f"Безопасный обратный элемент от 10.0: {val}")
    except ValidationError as e:
        print(f"Неожиданная ошибка: {e}")

    # Тест 2: Нарушение контракта (отрицательное число)
    try:
        safe_reciprocal(-5.0)
    except ValidationError as e:
        print(f"Поймали ожидаемую ошибку ValidationError: {e}")

    # Тест 3: Проверка иерархии исключений в SafeArray
    arr = SafeArray([1, 2, 3])
    try:
        arr.get_element(10)
    except OutOfBoundsError as e:
        print(f"Поймали ожидаемую ошибку OutOfBoundsError: {e}")
    except AiSDError as e:
        # Этот блок сработает, так как OutOfBoundsError наследуется от AiSDError
        print(f"Поймали базовую ошибку AiSDError: {e}")

    print("Все тесты второго дня успешно пройдены!")
