"""
В массиве целых чисел найдите количество пар элементов разного знака (соседние элементы).
"""


def count_different_sign_pairs(array):
    count = 0

    # Идем до len(array) - 1, чтобы не выйти за границы массива при проверке x[i+1]
    for i in range(len(array) - 1):
        # Если произведение соседних элементов отрицательное, у них разные знаки
        if array[i] * array[i + 1] < 0:
            count += 1

    return count
