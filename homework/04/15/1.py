# === ЗАДАЧА 1: Проверка палиндрома ===
# Дана строка. Вернуть True, если палиндром (игнорируя пробелы и регистр), иначе False.
# Сложность: O(n), не юзай срезы и встроенные функции для разворота

def is_palindrome(s: str) -> bool:
    """
    Проверить, палиндром ли строка.
    'A man a plan a canal Panama' -> True
    'hello' -> False
    """

    s = s.replace(" ","")
    s = s.lower()
    if s == s[::-1]:
        return True
    return False


# === ЗАДАЧА 2: Найти первое повторяющееся число ===
# Дан список чисел. Вернуть первое число, которое встречается больше одного раза.
# Если таких нет — вернуть None.
# Сложность: O(n) по времени, не юзай встроенные функции на базе хешей

def first_duplicate(nums: list) -> int | None:
    """
    Найти первое число, которое повторяется.
    [1, 2, 3, 2, 4, 1] -> 2
    [5, 6, 7] -> None
    """

    seen = set()
    for num in nums:
        if num in seen:
            return num
        seen.add(num)
    return None



# === ЗАДАЧА 3: Максимальная сумма подмассива ===
# Дан список целых чисел (могут быть отрицательные).
# Найти подмассив (непрерывный) с максимальной суммой.
# Вернуть эту сумму.
# Классическая задача — алгоритм Кадана.

def max_subarray_sum(nums: list) -> int:
    """
    Найти максимальную сумму подмассива.
    [-2, 1, -3, 4, -1, 2, 1, -5, 4] -> 6 (подмассив [4, -1, 2, 1])
    [-1, -2, -3] -> -1
    """

    if not nums:
        raise ValueError("Массив пуст")

    current_sum = max_sum = nums[0]
    for x in nums[1:]:
        current_sum = max(x, current_sum + x)
        max_sum = max(max_sum, current_sum)
    return max_sum



