def insertion_sort(arr):
    # Начинаем со второго элемента (индекс 1), так как первый считается "отсортированным"
    for i in range(1, len(arr)):
        key = arr[i]  # Текущий элемент, который будем "вставлять"
        j = i - 1

        # Сдвигаем элементы левой части (arr[0...i-1]), которые больше ключа,
        # на одну позицию вправо, чтобы освободить место для вставки
        while j >= 0 and arr[j] > key:
            arr[j + 1] = arr[j]
            j -= 1

        # Вставляем ключ на его законное место
        arr[j + 1] = key
    return arr
